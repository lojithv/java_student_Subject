public class Subject {

    private int sinhala;
    private int maths;

    public Subject(int sinhala, int maths) {
        this.sinhala = sinhala;
        this.maths = maths;
      
    }

    public int getSinhala() {
        return sinhala;
    }

    public void setSinhala(int sinhala) {
        this.sinhala = sinhala;
    }

    public int getMaths() {
        return maths;
    }

    public void setMaths(int maths) {
        this.maths = maths;
    }
}
